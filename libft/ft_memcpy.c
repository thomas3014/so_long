#include <stddef.h>
#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t len)
{
	char		*d;
	const char	*s;

	d = dest;
	s = src;
	if (dest == src)
		return (dest);
	while (len--)
	{
		*d = *s;
		d++;
		s++;
	}
	return (dest);
}
